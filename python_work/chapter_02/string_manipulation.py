# ========== TAB IN STRING ==========
print("\tPython")

# ========== NEWLINE IN STRING ==========
print("\nPython")

# ========== REMOVE WHITESPACE ON THE RIGHT SIDE OF A STRING ==========
name = "Damien Baubriau "
print(name.rstrip())

# ========== REMOVE WHITESPACE ON THE LEFT SIDE OF A STRING ==========
print(name.lstrip())

# ========== REMOVE PREFIXE ==========
url = "https://nostarch.com"
print(url.removeprefix('https://'))
